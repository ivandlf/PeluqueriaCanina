    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        //Todos los datos de la mascota
        String nombreMasco = txtNombre.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atenEsp = (String) cmbAtEsp.getSelectedItem();
        
        //los datos del due침o
        String nombreDuenio = txtNombreDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        //guardamos en la BD
        
       control.modificarMascota(masco, nombreMasco, raza, color, observaciones, 
               alergico, atenEsp, nombreDuenio, celDuenio);
       
       //Mostramos el mensaje
        mostrarMensaje("Se ha actualizado correctamente", "Info", "Modificar Datos");
        VerDatos pantallaVer = new VerDatos();
        pantallaVer.setVisible(true);
        pantallaVer.setLocationRelativeTo(null);
        this.dispose();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

